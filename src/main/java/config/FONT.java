package config;

public class FONT {
  public static java.awt.Font FONT_ROBOTO_PLAIN(int size) {
    return new java.awt.Font("Roboto", java.awt.Font.PLAIN, size);
  }
  public static java.awt.Font FONT_ROBOTO_BOLD(int size) {
    return new java.awt.Font("Roboto", java.awt.Font.BOLD, size);
  }
  public static java.awt.Font FONT_ROBOTO_ITALIC(int size) {
    return new java.awt.Font("Roboto", java.awt.Font.ITALIC, size);
  }
}
